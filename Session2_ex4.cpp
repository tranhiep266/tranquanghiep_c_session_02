#include <stdio.h>
int main(){
	int side = 5; // canh hinh vuong
	int girth = 4 * side; // chu vi hinh vuong
	int area = side * side; // dien tich hinh vuong
	printf("Canh cua hinh vuong: %d\n", side);
	printf("Chu vi cua hinh vuong: %d\n", girth);
	printf("Dien tich cua hinh vuong: %d\n", area);
	return 0;
}
